#include <stdio.h>
#include <math.h>

int main()
{
    int a = 3;
    int b = 2;

    printf("The value of a + b is: %d\n", a + b);
    printf("The value of a - b is: %d\n", a - b);
    printf("The value of a * b is: %d\n", a * b);
    printf("The value of a / b is: %d\n", a / b);

    printf("5 when divided by 2 leaves a remainder of %d\n", 4 % 9);
    printf("-5 when divided by 2 leaves a remainder of %d\n", 4 % 9);
    printf("5 when divided by -2 leaves a remainder of %d\n", 4 % -9);

    return 0;
}