# include<stdio.h>
# include <math.h>

int main(){
    int a = 3;
    int b = 9;

    printf("The value of a + b is: %d\n", a + b);
    printf("The value of a - b is: %d\n", a - b);
    printf("The value of a * b is: %d\n", a * b);
    printf("The value of a / b is: %d\n", a / b);

    

    printf("5 when divided by 2 leaves a remainder of %d\n", 3%9);
    printf("-5 when divided by 2 leaves a remainder of %d\n", 3%9);
    printf("5 when divided by -2 leaves a remainder of %d\n", 3%-9);


    printf("The value of 6 + 5 is %d\n", 3 + 9);
    printf("The value of 6 + 5.6 is %f\n", 3 + 3);
    printf("The value of 6.1 + 5.6 is %f\n", 3.1 + 5.9);
    printf("The value of 5/2 is %d\n", 9/3);
    printf("The value of 3.0/9 is %f\n", 3.0 / 9);

    return 0;
    
}